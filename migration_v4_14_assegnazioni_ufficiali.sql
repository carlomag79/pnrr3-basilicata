  denominazione_scuola text,
  nomina_coe boolean,
  preference_position integer,
  preferences_locked boolean
)
language sql
security definer
set search_path=public
as $$
  select
    a.id,a.insegnamento,a.graduatoria,a.posizione,a.punteggio,a.esito,
    a.provincia_assegnata,a.codice_scuola,a.denominazione_scuola,a.nomina_coe,
