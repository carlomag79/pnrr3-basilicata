create or replace function public.protect_locked_candidate_preferences()
returns trigger
language plpgsql
security definer
set search_path=public
as $$
begin
  if old.preferences_locked
     and not public.admin_is_current_user()
     and (
       new.candidature is distinct from old.candidature
       or new.preferenze_scuole is distinct from old.preferenze_scuole
       or new.provincia_assegnata is distinct from old.provincia_assegnata
       or new.provincia_1 is distinct from old.provincia_1
       or new.provincia_2 is distinct from old.provincia_2
       or new.comuni is distinct from old.comuni
     ) then
    raise exception 'Le preferenze sono state congelate dopo la conferma dell’assegnazione ufficiale';
  end if;
  return new;
end;
$$;

