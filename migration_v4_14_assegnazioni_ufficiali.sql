create or replace function public.admin_refresh_official_assignment_matches()
returns jsonb
language plpgsql
security definer
set search_path=public
as $$
declare
  item record;
  ids bigint[];
  matched integer:=0;
  ambiguous integer:=0;
  unmatched integer:=0;
begin
  perform public.require_admin();

  for item in
    select * from public.official_assignments
    where match_status not in ('confirmed','manual')
  loop
    select array_agg(c.id order by c.id)
    into ids
    from public.candidati c
    where exists(
      select 1
      from jsonb_to_recordset(coalesce(c.candidature,'[]'::jsonb)) as x(
        classe text,posizione integer,punteggio numeric
      )
      where x.classe=item.insegnamento
        and x.posizione=item.posizione
        and round(x.punteggio,2)=round(item.punteggio,2)
    );

    if coalesce(cardinality(ids),0)=1 then
      update public.official_assignments
      set candidate_id=ids[1],match_status='automatic'
      where id=item.id;
      matched:=matched+1;
    elsif coalesce(cardinality(ids),0)>1 then
      update public.official_assignments
      set candidate_id=null,match_status='ambiguous'
      where id=item.id;
      ambiguous:=ambiguous+1;
    else
      update public.official_assignments
      set candidate_id=null,match_status='unmatched'
      where id=item.id;
      unmatched:=unmatched+1;
    end if;
  end loop;

  return jsonb_build_object(
    'automatic',matched,'ambiguous',ambiguous,'unmatched',unmatched
  );
end;
$$;

create or replace function public.admin_list_official_assignments(
  p_class text default null,
  p_status text default null,
  p_query text default null
)
returns table(
  assignment_id bigint,
  insegnamento text,
  graduatoria text,
  posizione integer,
  punteggio numeric,
  esito text,
  cognome text,
  nome text,
  provincia_assegnata text,
  codice_scuola text,
  denominazione_scuola text,
  nomina_coe boolean,
  candidate_id bigint,
  match_status text,
  user_email text,
  preference_position integer
)
language plpgsql
security definer
set search_path=public
as $$
begin
  perform public.require_admin();

  return query
  select
    a.id,a.insegnamento,a.graduatoria,a.posizione,a.punteggio,a.esito,
    a.cognome,a.nome,a.provincia_assegnata,a.codice_scuola,
    a.denominazione_scuola,a.nomina_coe,a.candidate_id,a.match_status,
    u.email::text,
    (
      select pref.ordine
      from jsonb_to_recordset(coalesce(c.preferenze_scuole,'[]'::jsonb))
        as pref(classe text,codice_scuola text,ordine integer)
      where pref.classe=a.insegnamento
        and pref.codice_scuola=a.codice_scuola
      order by pref.ordine
      limit 1
    )::integer
  from public.official_assignments a
  left join public.candidati c on c.id=a.candidate_id
  left join auth.users u on u.id=c.user_id
  where (p_class is null or p_class='' or a.insegnamento=p_class)
    and (p_status is null or p_status='' or p_status='all' or a.match_status=p_status)
    and (
      p_query is null or trim(p_query)=''
      or a.cognome ilike '%'||trim(p_query)||'%'
      or a.nome ilike '%'||trim(p_query)||'%'
      or a.codice_scuola ilike '%'||trim(p_query)||'%'
      or a.denominazione_scuola ilike '%'||trim(p_query)||'%'
      or a.posizione::text=trim(p_query)
    )
  order by
    case a.insegnamento when 'AAAA' then 1 when 'ADAA' then 2 when 'EEEE' then 3 else 4 end,
    a.posizione,a.punteggio desc;
end;
$$;

create or replace function public.admin_confirm_official_assignment(
  p_assignment_id bigint,
  p_candidate_id bigint default null
)
returns boolean
language plpgsql
security definer
set search_path=public
as $$
declare
  target_candidate bigint;
begin
  perform public.require_admin();

  select coalesce(p_candidate_id,candidate_id)
  into target_candidate
  from public.official_assignments
  where id=p_assignment_id;

  if target_candidate is null then
    raise exception 'Seleziona un record da associare';
  end if;

  if not exists(select 1 from public.candidati where id=target_candidate) then
    raise exception 'Record non trovato';
  end if;

  update public.official_assignments
  set candidate_id=target_candidate,
      match_status=case when p_candidate_id is null then 'confirmed' else 'manual' end,
      confirmed_at=now(),
      confirmed_by=auth.uid()
  where id=p_assignment_id;

  update public.candidati
  set preferences_locked=true,
      updated_at=now()
  where id=target_candidate;

  return found;
end;
$$;

create or replace function public.admin_unlink_official_assignment(
  p_assignment_id bigint
)
returns boolean
language plpgsql
security definer
set search_path=public
as $$
declare old_candidate bigint;
begin
  perform public.require_admin();

  select candidate_id into old_candidate
  from public.official_assignments
  where id=p_assignment_id;

  update public.official_assignments
  set candidate_id=null,match_status='unmatched',
      confirmed_at=null,confirmed_by=null
  where id=p_assignment_id;

  if old_candidate is not null
     and not exists(
       select 1 from public.official_assignments
       where candidate_id=old_candidate
         and match_status in ('confirmed','manual')
     ) then
    update public.candidati
    set preferences_locked=false,updated_at=now()
    where id=old_candidate;
  end if;

  return found;
end;
$$;

